# --- –ù–û–í–ê–Ø –§–£–ù–ö–¶–ò–Ø: –ì–ï–ù–ï–†–ê–¶–ò–Ø –¶–ï–õ–ï–í–û–ì–û –°–û–í–ï–¢–ê ---
def generate_dynamic_advice(ex_type_name, full_context_text, prediction):
    """
    –°–æ–∑–¥–∞–µ—Ç —Ü–µ–ª–µ–≤–æ–π —Å–æ–≤–µ—Ç, –æ—Å–Ω–æ–≤—ã–≤–∞—è—Å—å –Ω–∞ —Ç–∏–ø–µ –∏—Å–∫–ª—é—á–µ–Ω–∏—è –∏ –ø–µ—Ä–µ–º–µ–Ω–Ω—ã—Ö –≤ –∫–æ–Ω—Ç–µ–∫—Å—Ç–µ.
    """
    
    # –†–µ–≥—É–ª—è—Ä–Ω–æ–µ –≤—ã—Ä–∞–∂–µ–Ω–∏–µ –¥–ª—è –∏–∑–≤–ª–µ—á–µ–Ω–∏—è –ø–µ—Ä–µ–º–µ–Ω–Ω—ã—Ö (Variables: key=value, key2=value2)
    variables_match = re.search(r'Variables:\s*(.*)', full_context_text)
    
    if not variables_match:
        # –ï—Å–ª–∏ –∫–æ–Ω—Ç–µ–∫—Å—Ç –Ω–µ –Ω–∞–π–¥–µ–Ω, –≤–æ–∑–≤—Ä–∞—â–∞–µ–º –æ–±—â–∏–π —Å–æ–≤–µ—Ç
        return ADVICE_MAP.get(prediction, "–ù–µ —É–¥–∞–ª–æ—Å—å –∏–∑–≤–ª–µ—á—å –ø–µ—Ä–µ–º–µ–Ω–Ω—ã–µ –¥–ª—è —Ü–µ–ª–µ–≤–æ–π —Ä–µ–∫–æ–º–µ–Ω–¥–∞—Ü–∏–∏.")
    
    # –ò–∑–≤–ª–µ—á–µ–Ω–Ω—ã–µ –ø–µ—Ä–µ–º–µ–Ω–Ω—ã–µ
    variables_str = variables_match.group(1).strip()
    
    # 1. –°—Ü–µ–Ω–∞—Ä–∏–π: ZeroDivisionError
    if ex_type_name == 'ZeroDivisionError':
        # –ò—â–µ–º –ø–µ—Ä–µ–º–µ–Ω–Ω—ã–µ, —Ä–∞–≤–Ω—ã–µ 0 (–Ω–∞–ø—Ä–∏–º–µ—Ä: count=0, denominator=0)
        zero_vars = re.findall(r'(\w+)=0(?:[,\]]|$)', variables_str)
        if zero_vars:
            # –í—ã–¥–µ–ª—è–µ–º –æ–¥–Ω—É –∏–ª–∏ –¥–≤–µ –ø–µ—Ä–µ–º–µ–Ω–Ω—ã–µ
            var_list = ', '.join(zero_vars[:2])
            return f"–ö—Ä–∏—Ç–∏—á–µ—Å–∫–∞—è –æ—à–∏–±–∫–∞ –¥–µ–ª–µ–Ω–∏—è –Ω–∞ –Ω–æ–ª—å. –ü–µ—Ä–µ–º–µ–Ω–Ω–∞—è(—ã–µ) **'{var_list}'** –±—ã–ª–∞(–∏) —Ä–∞–≤–Ω–∞(—ã) 0. –ü—Ä–æ–≤–µ—Ä—å—Ç–µ –ª–æ–≥–∏–∫—É –∏–Ω–∏—Ü–∏–∞–ª–∏–∑–∞—Ü–∏–∏."

    # 2. –°—Ü–µ–Ω–∞—Ä–∏–π: IndexError
    elif ex_type_name == 'IndexError':
        # –ò—â–µ–º 'index=' –∏ 'data=' –∏–ª–∏ 'list='
        index_match = re.search(r'index=([^,\s]+)', variables_str)
        list_match = re.search(r'(data|list|cart_items)=', variables_str)
        
        if index_match and list_match:
            index_val = index_match.group(1)
            return f"–í—ã—Ö–æ–¥ –∑–∞ –≥—Ä–∞–Ω–∏—Ü—ã –º–∞—Å—Å–∏–≤–∞. –ü–æ–ø—ã—Ç–∫–∞ –¥–æ—Å—Ç—É–ø–∞ –∫ –∏–Ω–¥–µ–∫—Å—É **{index_val}**. –ü—Ä–æ–≤–µ—Ä—å—Ç–µ –¥–ª–∏–Ω—É –º–∞—Å—Å–∏–≤–∞, –ø—Ä–µ–∂–¥–µ —á–µ–º –æ–±—Ä–∞—â–∞—Ç—å—Å—è –∫ —ç–ª–µ–º–µ–Ω—Ç—É."

    # 3. –°—Ü–µ–Ω–∞—Ä–∏–π: FileNotFoundError
    elif ex_type_name == 'FileNotFoundError':
        path_match = re.search(r'(path|config_path)=([^,\s]+)', variables_str)
        if path_match:
            path_val = path_match.group(2).strip()
            return f"–§–∞–π–ª –Ω–µ –Ω–∞–π–¥–µ–Ω. –ü—Ä–æ–≤–µ—Ä—å—Ç–µ —Å—É—â–µ—Å—Ç–≤–æ–≤–∞–Ω–∏–µ –ø—É—Ç–∏ **{path_val}**. –í–æ–∑–º–æ–∂–Ω–æ, –Ω–µ–≤–µ—Ä–Ω—ã–π —Ä–∞–±–æ—á–∏–π –∫–∞—Ç–∞–ª–æ–≥."
            
    # –ï—Å–ª–∏ —Ü–µ–ª–µ–≤–æ–µ –ø—Ä–∞–≤–∏–ª–æ –Ω–µ —Å—Ä–∞–±–æ—Ç–∞–ª–æ, –≤–æ–∑–≤—Ä–∞—â–∞–µ–º –æ–±—â–∏–π —Å–æ–≤–µ—Ç
    return ADVICE_MAP.get(prediction, "–°—Ç–∞–Ω–¥–∞—Ä—Ç–Ω–∞—è —Ä–µ–∫–æ–º–µ–Ω–¥–∞—Ü–∏—è, –æ—Å–Ω–æ–≤–∞–Ω–Ω–∞—è –Ω–∞ –∫–ª–∞—Å—Å–µ.")


def smart_analyze(ex_type, ex_value, ex_traceback):
    """
    –û—Å–Ω–æ–≤–Ω–∞—è —Ñ—É–Ω–∫—Ü–∏—è –∞–Ω–∞–ª–∏–∑–∞ –∏ –≤—ã–≤–æ–¥–∞ –æ—Ç—á–µ—Ç–∞.
    """
    if not model:
        print("–ú–æ–¥–µ–ª—å –Ω–µ –∑–∞–≥—Ä—É–∂–µ–Ω–∞. –°–Ω–∞—á–∞–ª–∞ –∑–∞–ø—É—Å—Ç–∏—Ç–µ trainer.py")
        return

    # 1. –ó–∞—Ö–≤–∞—Ç—ã–≤–∞–µ–º —Ç–µ–∫—Å—Ç –æ—à–∏–±–∫–∏ + –ø–µ—Ä–µ–º–µ–Ω–Ω—ã–µ
    raw_text = analyzer_collector.capture_exception(ex_type, ex_value, ex_traceback, label=None)
    
    # 2. –ß–∏—Å—Ç–∏–º —Ç–µ–∫—Å—Ç
    cleaned_input = clean_text(raw_text)
    
    # 3. –ü—Ä–µ–¥—Å–∫–∞–∑—ã–≤–∞–µ–º
    prediction = model.predict([cleaned_input])[0]
    proba = model.predict_proba([cleaned_input]).max()
    
    # 4. –ò–∑–≤–ª–µ–∫–∞–µ–º –∫–æ–Ω—Ç–µ–∫—Å—Ç –ø–µ—Ä–µ–º–µ–Ω–Ω—ã—Ö –¥–ª—è –æ—Ç–æ–±—Ä–∞–∂–µ–Ω–∏—è
    variables_match = re.search(r'Variables:\s*(.*)', raw_text)
